function icon(name, size) {
    let svg = ICONS[name] || '';
    if (size && svg) {
        svg = svg.replace(/width="18"/g, `width="${size}"`).replace(/height="18"/g, `height="${size}"`);
    }
    return svg;
}

// --- i18n engine ---

function getLang() {
    return localStorage.getItem('jt-glogarch-lang') || 'en';
}

function setLang(lang) {
    localStorage.setItem('jt-glogarch-lang', lang);
    applyI18n();
    document.dispatchEvent(new CustomEvent('langchange', {detail: {lang}}));
}

function t(key) {
    const lang = getLang();
    return (I18N[lang] && I18N[lang][key]) || (I18N.en[key]) || key;
}

function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const iconName = el.getAttribute('data-icon');
        const prefix = iconName ? icon(iconName) + ' ' : '';
        el.innerHTML = prefix + t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('[data-i18n-opt]').forEach(el => {
        el.textContent = t(el.getAttribute('data-i18n-opt'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.getAttribute('data-i18n-title'));
    });
    const switcher = document.getElementById('lang-select');
    if (switcher) switcher.value = getLang();
    document.documentElement.lang = getLang() === 'zh-TW' ? 'zh-TW' : 'en';
}

// --- Theme engine ---

function getTheme() {
    return localStorage.getItem('jt-glogarch-theme') || 'light';
}

function setTheme(theme) {
    localStorage.setItem('jt-glogarch-theme', theme);
    applyTheme();
}

function toggleTheme() {
    setTheme(getTheme() === 'dark' ? 'light' : 'dark');
}

function applyTheme() {
    document.documentElement.setAttribute('data-theme', getTheme());
    const btn = document.getElementById('theme-toggle');
    if (btn) {
        const isDark = getTheme() === 'dark';
        btn.innerHTML = isDark ? icon('sun') : icon('moon');
        btn.title = isDark ? t('theme_light') : t('theme_dark');
    }
}

document.addEventListener('DOMContentLoaded', () => {
    applyTheme();
    applyI18n();
});
